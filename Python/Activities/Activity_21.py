import pytest
 

def test_addition():
	num1 = 1000
	num2 = 15
	sum = num1 + num2
	assert sum == 1015
def subtraction_test():
  
	num1 = 100
	num2 = 35
	diff = num1 - num2
	assert diff == 65
def test_multiplication():
  
	num1 = 5
	num2 = 10
	prod = num1 * num2
	assert prod == 50
 

def test_division():
	num1 = 100
	num2 = 5
	quot = num1 / num2
	assert quot == 20